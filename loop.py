# show a demo on how to use while loop in python
i = 0
while i < 5:
  print(f"Value of i: {i}")
  i += 1

# Using a for loop
for j in range(5):
  print(f"Value of j: {j}")

# Using a for loop with an array
fruits = ["apple", "banana", "orange"]
for fruit in fruits:
  print(f"I love {fruit}s!")

# show how to use `range` in python
for i in range(10):
  print(f"Value of i: {i}")

# show how to use `for` loop in python
fruits = ["apple", "banana", "orange"]
for fruit in fruits:
  print(f"I love {fruit}s!")

# show how to use `enumerate` in python
fruits = ["apple", "banana", "orange"]
for index, fruit in enumerate(fruits):
  print(f"I love {fruit}s at index {index}")
